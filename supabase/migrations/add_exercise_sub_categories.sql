-- 1. exercises 테이블이 없으면 생성
CREATE TABLE IF NOT EXISTS exercises (
  id TEXT PRIMARY KEY,
  body_part TEXT,
  equipment TEXT,
  name_en TEXT,
  name TEXT NOT NULL,
  gif_url TEXT,
  sub_category TEXT
);

-- 2. 이미 테이블이 있는 경우 sub_category 컬럼만 추가
ALTER TABLE exercises ADD COLUMN IF NOT EXISTS sub_category TEXT;

